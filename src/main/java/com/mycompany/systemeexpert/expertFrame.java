    public void afficherRegle(){
        nouveauxRegles.clear();
        nouveauxRegles = MyConnection.nouveauRegles();
        
        if(nouveauxRegles.isEmpty()){
            newRegleLabel.setText("il n'y a aucune demande a ce moment");
            newFaitTextField.setEnabled(false);
            newFaitTextField.setEditable(false);
        }else{
            newRegleLabel.setText(nouveauxRegles.get(0));
        }
        
    }
    
