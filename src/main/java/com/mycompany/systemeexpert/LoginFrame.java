    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = usernamefield.getText();
        String password = String.valueOf(passwordField.getPassword());
        
        if(username.isEmpty() ){
            JOptionPane.showMessageDialog(this, "Veulliez entrer un username");
        }else if(password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Veulliez entrer votre mot de pass");
        }
        else{
            
            PreparedStatement ps;
            ResultSet rs;
            String query = "SELECT * FROM `users` WHERE `USERNAME` =? AND `PASSWORD`= ?";
            
            try {
                ps = connection.prepareStatement(query);
                
                ps.setString(1, username); 
                ps.setString(2, password);
                rs = ps.executeQuery();
                
                
                
                if (rs.next()) {
                    boolean isExpert = rs.getBoolean("IS_EXPERT");;
                    if(isExpert){
                        Expert expert = new Expert();
                        expert.setName(rs.getString("USERNAME"));
                        expert.setPassword(rs.getString("PASSWORD"));
                        expert.setId(rs.getInt("ID"));
                        expert.setEmail(rs.getString("EMAIL"));
                     
                        expertFrame frame = new expertFrame(expert);
                        frame.setVisible(true);
                        
                    }else{
                        
                    regles = MyConnection.databaseSnapshot();
                        
                     User user = new User();
                     user.setName(rs.getString("USERNAME"));
                     user.setPassword(rs.getString("PASSWORD"));
                     user.setId(rs.getInt("ID"));
                     user.setEmail(rs.getString("EMAIL"));
                     
                     System.out.println(regles);
                     userFrame reg = new userFrame(user,regles);
                     
                     }
                     
                     this.dispose();
                     
                     
                            
                     
                     
                    
                }
                else{
                    JOptionPane.showMessageDialog(null, "Les information saisie sont incorect");
                    usernamefield.setText("");
                    passwordField.setText("");
                }
            } catch (SQLException ex) {
                Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            
           
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

