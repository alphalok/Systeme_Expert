    private void ValiderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValiderButtonActionPerformed
        String newFait = newFaitTextField.getText();
        if(newFait.isEmpty()){
            JOptionPane.showMessageDialog(null, "veuiller entrer le fait");
        }
        else{

            PreparedStatement ps;
            String query ="UPDATE `reglesfait` SET `Fait`= ? WHERE `Regles`=?";
            try {
                ps= MyConnection.getConnection().prepareStatement(query);

                
                ps.setString(1, newFait);
                ps.setString(2, newRegleLabel.getText().trim());
                

                if(ps.executeUpdate()>0){
                    JOptionPane.showMessageDialog(null, "Fait a eté ajouté avec succès");
                }
            } catch (SQLException ex) {
                System.out.println(ex);
                
            }
            
            expertFrame frame = new expertFrame(expert);
            frame.setVisible(true);
            this.dispose();
            
        }
        
        
    }//GEN-LAST:event_ValiderButtonActionPerformed

